def main():
    t0 = time.time()
    NSH = 4          # production cutoff radius (validated in Block 8)
    ok = []

    print("=" * 78)
    print(" TBG · BISTRITZER–MacDONALD CONTINUUM MODEL")
    print("=" * 78)
    print(f"  a = {A_CC} nm | ħv_F = {HBARVF:.5f} eV·nm | |K| = {K_DIRAC:.4f} nm⁻¹")
    print(f"  w_AA = {1e3*W_AA:.1f} meV | w_AB = {1e3*W_AB:.1f} meV "
          f"(η = w_AA/w_AB = {W_AA/W_AB:.3f})")

    # ---- 1) Twist-angle sweep: W(θ) and v*(θ) ------------------------------
    print("\n[1/4] θ sweep (6×6 grid over the mini-BZ, N_sh = 4)…")
    th_grueso = np.arange(0.70, 1.62, 0.02)
    W_grueso = np.array([ancho_bandas_centrales(t, NSH) for t in th_grueso])
    t_c = th_grueso[np.argmin(W_grueso)]
    th_fino = np.arange(t_c - 0.06, t_c + 0.0625, 0.005)
    W_fino = np.array([ancho_bandas_centrales(t, NSH) for t in th_fino])
    theta_estrella = th_fino[np.argmin(W_fino)]

    thetas = np.unique(np.concatenate([th_grueso, th_fino]))
    Ws = np.array([W_grueso[np.where(th_grueso == t)[0][0]]
                   if t in th_grueso else W_fino[np.where(th_fino == t)[0][0]]
                   for t in thetas])
    vs = np.array([velocidad_renormalizada(t, NSH) for t in thetas])

    W_estrella = ancho_bandas_centrales(theta_estrella, NSH, malla=12)
    print(f"      θ* = {theta_estrella:.3f}°  →  W = {1e3*W_estrella:.2f} meV "
          f"(12×12 grid),  v*/v_F = "
          f"{velocidad_renormalizada(theta_estrella, NSH):.4f}")
    val1 = 1.05 <= theta_estrella <= 1.10
    ok.append(val1)
    print(f"      VALIDATION 1 — minimum of W in 1.05° ≤ θ* ≤ 1.10°: "
          f"{'✓ PASS' if val1 else '✗ FAIL'}")
    figura_barrido(thetas, Ws, vs, theta_estrella, "fig2_barrido_theta.png")

