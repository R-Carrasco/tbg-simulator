GRIS, ROJO, AZUL = "#8a93a6", "#d6453d", "#2660a4"


def figura_bandas(theta_grande, theta_magico, n_sh, archivo):
    fig, axes = plt.subplots(1, 2, figsize=(10.5, 4.4))
    for ax, th in zip(axes, [theta_grande, theta_magico]):
        dist, E, ticks, lab = bandas_camino(th, n_sh)
        nb = E.shape[1] // 2
        for b in range(E.shape[1]):
            if abs(E[:, b]).min() < 0.30:
                ax.plot(dist, 1e3 * E[:, b], color=GRIS, lw=0.9)
        for b in (nb - 1, nb):
            ax.plot(dist, 1e3 * E[:, b], color=ROJO, lw=1.8)
        W = 1e3 * (E[:, nb].max() - E[:, nb - 1].min())
        ax.set_xticks(ticks); ax.set_xticklabels(lab)
        for t in ticks[1:-1]:
            ax.axvline(t, color="k", lw=0.5, alpha=0.3)
        ax.set_xlim(dist[0], dist[-1]); ax.set_ylim(-250, 250)
        ax.set_title(f"θ = {th:.2f}°   ·   W(path) = {W:.1f} meV")
        ax.set_ylabel("E  [meV]")
    # inset zoom of the flat bands in the magic-angle panel
    dist, E, ticks, lab = bandas_camino(theta_magico, n_sh)
    nb = E.shape[1] // 2
    axz = axes[1].inset_axes([0.58, 0.06, 0.40, 0.34])
    for b in (nb - 1, nb):
        axz.plot(dist, 1e3 * E[:, b], color=ROJO, lw=1.2)
    axz.set_xticks(ticks); axz.set_xticklabels(lab, fontsize=7)
    axz.set_ylim(-8, 8); axz.tick_params(labelsize=7); axz.grid(alpha=0.2)
    axz.set_title("flat-band zoom", fontsize=7)
    fig.suptitle("TBG BM bands (one valley) — path Γ→K→M→K'→Γ of the moiré mini-BZ")
    fig.tight_layout()
    fig.savefig(archivo)
    plt.close(fig)


def figura_barrido(thetas, Ws, vs, theta_estrella, archivo):
    fig, ax = plt.subplots(figsize=(7.2, 4.4))
    ax.semilogy(thetas, 1e3 * Ws, "o-", color=AZUL, ms=3.5, lw=1.4,
                label="W(θ): width of the 2 central bands")
    ax.axvspan(1.05, 1.10, color=ROJO, alpha=0.10,
               label="expected window 1.05–1.10°")
    ax.axvline(theta_estrella, color=ROJO, lw=1.4, ls="--",
               label=f"θ* = {theta_estrella:.3f}°  (minimum of W)")
    ax.set_xlabel("twist angle θ  [degrees]")
    ax.set_ylabel("W  [meV]  (log scale)")
    ax2 = ax.twinx()
    ax2.plot(thetas, vs, "s--", color="#3d8c5c", ms=3, lw=1.1, alpha=0.85)
    ax2.set_ylabel("v*/v_F  (renormalized Dirac velocity)", color="#3d8c5c")
    ax2.tick_params(axis="y", colors="#3d8c5c")
    ax2.set_ylim(bottom=-0.02)
    ax2.grid(False)
    ax.set_title("Flattening of the central bands and first magic angle")
    ax.legend(loc="upper center", fontsize=8)
    fig.tight_layout()
    fig.savefig(archivo)
    plt.close(fig)


def figura_dos(lista_th, n_sh, archivo):
    fig, ax = plt.subplots(figsize=(7.2, 4.8))
    colores = plt.cm.plasma(np.linspace(0.10, 0.78, len(lista_th)))
    paso = 1.15
    for i, th in enumerate(lista_th):
        eje, rho = dos(th, n_sh=n_sh)
        ax.plot(1e3 * eje, rho / rho.max() + paso * i, color=colores[i], lw=1.3)
        ax.text(62, paso * i + 0.55, f"θ = {th:.2f}°", color=colores[i],
                fontsize=9, weight="bold")
    ax.axvline(0, color="k", lw=0.7, alpha=0.5)
    ax.set_xlabel("E  [meV]")
    ax.set_ylabel("Normalized DOS (offset curves)")
    ax.set_yticks([])
    ax.set_title("Collapse of the van Hove singularities toward E = 0\n"
                 "as the magic angle is approached (one valley, one spin)")
    fig.tight_layout()
    fig.savefig(archivo)
    plt.close(fig)


def figura_convergencia(cortes, Ws, dims, archivo):
    fig, axes = plt.subplots(1, 2, figsize=(9.5, 3.9))
    axes[0].plot(cortes, 1e3 * Ws, "o-", color=AZUL)
    axes[0].axhline(1e3 * Ws[-1], color="k", lw=0.7, ls=":")
    axes[0].set_xlabel("cutoff radius  N_sh  (in units of |b₁|)")
    axes[0].set_ylabel("W  [meV]")
    axes[0].set_title("Flat-band W vs. cutoff")
    err = np.abs(Ws[:-1] - Ws[-1]) * 1e3
    axes[1].semilogy(cortes[:-1], np.maximum(err, 1e-9), "s-", color=ROJO)
    axes[1].set_xlabel("N_sh")
    axes[1].set_ylabel("|W(N) − W(N_max)|  [meV]")
    axes[1].set_title("Truncation error")
    for ax, c, d in [(axes[0], cortes, dims)]:
        for x, dd, y in zip(c, d, 1e3 * Ws):
            ax.annotate(f"dim {dd}", (x, y), textcoords="offset points",
                        xytext=(0, 8), fontsize=7, ha="center", color="#555")
    fig.suptitle("Convergence of the diagonalization with respect to the momentum cutoff")
    fig.tight_layout()
    fig.savefig(archivo)
    plt.close(fig)


